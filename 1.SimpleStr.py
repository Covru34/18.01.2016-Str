# інтераці по рядку за допомогою циклу for
print("--- Інтерація у рядку ---")
name = "Василь"
for char in name:
    print(char)

# чи входить рядок у підрядок
print("си" in name) # True - данний підрядок є у рядку
print("ка" in name) # False - діного підрядка немає у рядку

